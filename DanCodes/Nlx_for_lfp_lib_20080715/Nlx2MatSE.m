% Help for file Nlx2MatSE.dll
%
% Nlx2MatSE.dll is a matlab mex file compilation of matlab and C++ source code.
% C++ source code is used to read in data from a file and the data is them sent back to the matlab
% command window in the form of matlab arrays or matrices.  Nlx2MatSE takes input from a file
% and extracts the data from the file that the user requests.  A user can request any combination
% of Neuralynx data to be returned from the file selected.
%
% There are 3 ways in which Nlx2MatSE.dll can process a file.  
% 
%     1.  Whole File Processing - All the file is read into matlab in the form of 1 - 6 possible output variables.
%         Ex.  [TimeStamps, ScNumbers, CellNumbers, Params, DataPoints, NlxHeader] = Nlx2MatSE('c:\path\filename',1,1,1,1,1,1);
%     2.  Range File Processing - A selected range of the file is read into matlab based on lower and upper timestamp limit.
%         This requires passing 2 additional paramters.
%         Ex.  [TimeStamps, ScNumbers, CellNumbers, Params, DataPoints, NlxHeader] = Nlx2MatSE('c:\path\filename',1,1,1,1,1,1,MinTsBound,MaxTsBound);
%     3.  Selective File Processing - Any Combination of records can be extracted from the selected file.
%         The user passes in a Matlab double matrix or array consisting of timestamps.  Any records in the file will
%         whose timestamp matches 1 within the array will be extracted from the file and put into matlab.
%         This requires 1 extra paramter than normal, a double matrix or array.
%         Ex.  [TimeStamps, ScNumbers, CellNumbers, Params, DataPoints, NlxHeader] = Nlx2MatSE('c:\path\filename',1,1,1,1,1,1,InputTimestampArray);
%         Note:  The timestamps in the array must be positive increasing values.
% 
% Nlx2MatSE.dll accepts 7, 8 or 9 parameters.
% The 7 required parameters are always the first 7 parameters.
% The first paramter is the filename for which one is to extract data.
% The filename must be a string.
% The filename being input must be in the current directory or the entire path must be specified.
% The filename and/or path name must be in single quotes.
% The file being entered must be a file of SERecs (Single Electrode data)
% The next 6 paramters represent the data fields within the file.  They are also directly
% related to the output parameters ( See below ).  Each value represents whether to extract
% that fields' data from the file and store it in matlab.  If a zero is entered for a field,
% then the data will not be extracted.  If a 1 is entered then the data will be extracted.
% The number of output arguments must match the number of 1's being passed in.  An output 
% arguments name should only be placed within the square brackes ( [ ] ) if it is going to
% be read into.
% Some examples:
%     1.  To extract all fields from the file, format is as follows:
%         [TimeStamps, ScNumbers, CellNumbers, Params, DataPoints, NlxHeader] = Nlx2MatSE('c:\path\filename',1,1,1,1,1,1);
%     2.  To extract just TimeStamps, format is as follows:
%         [TimeStamps] = Nlx2MatSE('c:\path\filename',1,0,0,0,0,0);
%     3.  To extract Timestamp, CellNumbers and DataPoints, format is as follows:
%         [TimeStamps, CellNumbers, DataPoints] = Nlx2MatSE('c:\path\filename',1,0,1,0,1,0);
% This functionality can be used with any of the above 3 ways to process a file.
%
% The 6 variables Nlx2MatSE outputs are:
% TimeStamps, ScNumbers, CellNumbers, Params, DataPoints and NlxHeader in that order.
% TimeStamps, ScNumbers and CellNumbers are one dimensional matrices,
% Params is a 2 dimensional matrix and DataPoints is a 3 dimensional matrix.
% NlxHeader is a one dimensional cell array, while all others are numeric.
% For multidimensional matrices, data is output in vertical form for each element.
%
% Note: NlxHeaders are Neuralynx Headers generated by Neuralynx Software, however they may not always be present in a Neuralynx file.
%       If a NlxHeader is not found an empty cell matrix is returned.
%
% Some helpful matlab usage tips and commands( commands will be written inside single quotes ' ':
%
%  'whos' - This command will list all current matlab variables, their dimensions and byte sizes. For Example:
%
% >> whos
%    Name                     Size         Bytes  Class
%
%  ChannelNumbers           1x101          808  double array
%  NlxHeader               17x1           2366  cell array
%  NumberValidSamples       1x101          808  double array
%  SampleFrequencies        1x101          808  double array
%  Samples                512x101       413696  double array
